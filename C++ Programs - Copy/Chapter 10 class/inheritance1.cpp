/*  Inheritance : In C++, inheritance is a process in which one object acquires all the
                properties and behaviors of its parent object automatically. 

    Single inheritance : A derived class with only one base class
    Multiple inheritance : A derived class with more than one base class ( 2 baap )
    Hierarchical inheritance : Several derived classes with single base class  ( 1 baap )
    Multilevel inheritance : Deriving class from already derived class   ( A-> B -> C)
    Hybrid inheritance : It is combination of Multiple inheritance and Multilevel inheritance
*/
